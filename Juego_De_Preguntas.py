class jugador:

    TABLERO = [[1, 2, 3, 4, 5], [10, 9, 8, 7, 6], [
        11, 12, 13, 14, 15], [20, 19, 18, 17, 16]]

    def __init__(self, simbolo):

        self.posicion = [0,0]
        self.lugar= 1
        self.aciertos = 0

        self.simbolo = simbolo

    @staticmethod
    def look_for(lista, valor):

        try:

            lista.index(valor)

            return True

        except ValueError:

            return False

    def mover(self, movimiento):

        x, y = self.posicion

        actual = jugador.TABLERO[x][y]

        actual += movimiento

        if(actual <= 20 and actual >= 1):

            for i in range(len(jugador.TABLERO)):

                if self.look_for(jugador.TABLERO[i], actual):

                    self.posicion = [i, jugador.TABLERO[i].index(actual)]
                    self.lugar=actual
                    return
        elif(movimiento<1):
            self.posicion=[0,0]
            self.lugar=1    
            return
        self.lugar = actual-movimiento
        print("\n::No es posible realizar el movimiento, fuera de los limites de la tabla::")

